    methods: {
      save () {
        if (this.form.name.length === 0) {
            this.$message.error('氏名が入力されていません')
            return
        }
        if (this.form.email.length === 0) {
            this.$message.error('メールアドレスが入力されていません')
            return
        }
        let re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
        if (!re.test(this.form.email)) {
            this.$message.error('正しいメールアドレスを入力してください')
            return
        }

        let self = this
        let post_data = {
          subject: self.form.subject,
          name: self.form.name,
          email: self.form.email,
          phone: self.form.phone,
          detail: self.form.detail
        }
        console.log(post_data)
        let method = 'post'
        let url = 'home/'
        self.loading = true
        self.$axios[method](url, post_data).then(response => {
          self.$message('お問い合わせ内容の送信が完了しました。担当者からのメールをお待ち下さい。')
          this.form = ''
          self.loading = false
        }).catch(error => {
          if (error.response) {
              let e = error.response.data
              self.$message.error(e.message + ' [' + e.code + ']')
          }
        })
      }
    },
  }
</script>

